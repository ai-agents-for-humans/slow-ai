@router.post("/api/runs/launch")
async def launch_run(project_id: str = Form(...)):
    brief_path = Path("output") / project_id / "problem_brief.json"
    if not brief_path.exists():
        return JSONResponse({"error": "Project not found"}, status_code=404)
    try:
        run_id = _start_research(project_id)
    except Exception as e:
        return JSONResponse({"error": str(e)}, status_code=500)
    return {"run_id": run_id, "project_id": project_id}


# ── Conversation store (in-memory, keyed by run_id) ──────────────────────────
_conv_sessions: dict[str, list] = {}


def _find_project(run_id: str) -> str | None:
    for runs_file in Path("output").glob("*/runs.jsonl"):
        try:
            for line in runs_file.read_text(encoding="utf-8").splitlines():
                if run_id in line:
                    return runs_file.parent.name
        except Exception:
            pass
    return None


@router.get("/run/{run_id}", response_class=HTMLResponse)
async def run_page(request: Request, run_id: str):
    run_dir = Path("runs") / run_id
    if not run_dir.exists():
        return HTMLResponse("Run not found", status_code=404)

    status_data = _read_live(run_id, "status.json", {"status": "initializing"})
    status = status_data.get("status", "initializing")

    brief_path = run_dir / "input_brief.json"
    brief = {}
    if brief_path.exists():
        try:
            brief = json.loads(brief_path.read_text(encoding="utf-8"))
        except Exception:
            pass

    # For completed runs, assemble results inline so Results tab renders immediately
    post_run = _build_post_run(run_id) if status in _TERMINAL else None

    return _templates.TemplateResponse(
        "views/run.html",
        {
            "request": request,
            "run_id": run_id,
            "brief": brief,
            "status": status,
            "post_run": post_run,
            "start_tab": "results" if status in _TERMINAL else "run",
        },
    )


