@router.get("/api/runs/{run_id}/export")
async def export_run(run_id: str, request: Request):
    """Export the final research report as a standalone HTML file."""
    import html as _html

    import mistune

    run_dir = Path("runs") / run_id
    if not run_dir.exists():
        return JSONResponse({"error": "Run not found"}, status_code=404)

    doc_path = run_dir / "live" / "final_report.md"
    if not doc_path.exists():
        return JSONResponse({"error": "Final report not yet generated"}, status_code=404)

    document = doc_path.read_text(encoding="utf-8")

    brief_path = run_dir / "problem_brief.json"
    brief = {}
    if brief_path.exists():
        try:
            brief = json.loads(brief_path.read_text(encoding="utf-8"))
        except Exception:
            pass

    rendered_body = mistune.html(document)
    goal = _html.escape(brief.get("goal", run_id))
    domain = _html.escape(brief.get("domain", ""))
    run_date = datetime.now(UTC).strftime("%B %d, %Y")

    html_content = _templates.TemplateResponse(
        "export/run_export.html",
        {
            "request": request,
            "goal": goal,
            "domain": domain,
            "run_id": run_id,
            "run_date": run_date,
            "rendered_body": rendered_body,
        },
    ).body.decode()

    safe_goal = "".join(c if c.isalnum() or c in "-_ " else "" for c in brief.get("goal", run_id))
    filename = f"research-{safe_goal[:50].strip().replace(' ', '-')}.html"

    from fastapi.responses import Response

    return Response(
        content=html_content,
        media_type="text/html",
        headers={"Content-Disposition": f'attachment; filename="{filename}"'},
    )


@router.get("/api/runs/{run_id}/phase-summary")
async def run_phase_summary(run_id: str, node_type: str = ""):
    """Return the phase synthesis for a DAG phase node, matched by node_type slug."""
    summaries = _read_live(run_id, "phase_summaries.json", [])
    if not summaries:
        return JSONResponse({"error": "not_complete"}, status_code=404)

    # node_type looks like 'phase_reddit_need_discovery'; strip prefix to get slug
    slug = node_type[len("phase_"):] if node_type.startswith("phase_") else node_type

    for s in summaries:
        phase_slug = (s.get("phase_name") or "").lower().replace(" ", "_")
        if phase_slug == slug:
            return s

    return JSONResponse({"error": "not_found"}, status_code=404)


@router.get("/api/runs/{run_id}/interview")
async def run_interview_data(run_id: str):
    """Return the interview conversation and context graph that launched this run."""
    from app.api.interview import _sessions, _graph_for_cytoscape
    from slow_ai.models import ContextGraph

    project_id = _find_project(run_id)
    if not project_id:
        return {"conversation_log": [], "graph_elements": []}

    # Try in-memory sessions first
    session = next((s for s in _sessions.values() if s.get("project_id") == project_id), None)
    if session:
        graph = session.get("draft_graph")
        return {
            "conversation_log": session.get("conversation_log", []),
            "graph_elements": _graph_for_cytoscape(graph) if graph else [],
        }

    # Fall back to disk
    interviews_dir = Path("output") / "interviews"
    if interviews_dir.exists():
        for session_file in interviews_dir.glob("*/session.json"):
            try:
                meta = json.loads(session_file.read_text(encoding="utf-8"))
                if meta.get("project_id") != project_id:
                    continue
                conv_log = []
                log_file = session_file.parent / "conversation_log.json"
                if log_file.exists():
                    conv_log = json.loads(log_file.read_text(encoding="utf-8"))
                elements = []
                graph_file = session_file.parent / "draft_graph.json"
                if graph_file.exists():
                    try:
                        graph = ContextGraph.model_validate_json(graph_file.read_text(encoding="utf-8"))
                        elements = _graph_for_cytoscape(graph)
                    except Exception:
                        pass
                return {"conversation_log": conv_log, "graph_elements": elements}
            except Exception:
                continue

    return {"conversation_log": [], "graph_elements": []}


@router.get("/api/runs/{run_id}/stream")
async def stream_run(run_id: str, request: Request):
    run_dir = Path("runs") / run_id
    if not run_dir.exists():
        return JSONResponse({"error": "Run not found"}, status_code=404)

    async def generator():
        last_log_count = 0
        while True:
            if await request.is_disconnected():
                break

            state = _run_state(run_id)

            # Only send new log lines to avoid resending everything
            all_log = state["log"]
            new_log = all_log[last_log_count:]
            last_log_count = len(all_log)
            state["new_log"] = new_log

            yield {"data": json.dumps(state, default=str)}

            if state["status"] in _TERMINAL:
                # Send one final update then close
                break

            await asyncio.sleep(_POLL_INTERVAL)

    return EventSourceResponse(generator())
